cumlative<-function(n){
  sum<-0
  start<-1
  while(start<n+1){
    sum<-sum+start
    start<-start+1
  }
  print(sum)
  sum<-0
  start<-1
  for(i in start:n){
    sum<-sum+i
  }
  print(sum)
}
