max2nd<-function(nums){
  maxNum<-max(nums)
  array<-c()
  for(i in nums){
    if(maxNum-i!=0){
      array<-c(array,maxNum-i)
    }
  }
  len1<-length(array)
  len2<<-length(array)
  return(maxNum-min(array))
}
